// Helper function to get translation by key
export const getTranslation = (language, key) => {
  const keys = key.split('.');
  let value = translations[language] || translations.en;

  for (const k of keys) {
    value = value[k];
    if (!value) {
      console.warn(`Translation key not found: ${key} for language: ${language}`);
      return key;
    }
  }

  return value;
};

// Helper function to get all translations for a section
export const getSectionTranslations = (language, section) => {
  return translations[language]?.[section] || translations.en[section];
};

export default translations;
